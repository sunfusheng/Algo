package com.sunfusheng.algo.common.model;

/**
 * @author sunfusheng
 * @since 2022/06/05
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
